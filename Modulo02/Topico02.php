                            <br><p>Agora, vamos analisar o que diz a Constituição Federal de 1988, <a href="http://cursos.egp.ce.gov.br/pluginfile.php/25524/mod_folder/content/0/art165.pdf?forcedownload=1" target="_blank">art. 165, §1°</a> sobre o PPA:</p>

                            <br><div class="bd-callout bd-callout-success">
                                <p class="Texto">
                                    <strong>A lei que instituir o plano plurianual estabelecerá, de forma regionalizada, as diretrizes, objetivos e metas da administração pública federal para as despesas de capital e outras delas decorrentes e para as relativas aos programas de duração continuada.</strong>
                                    <p class="text-end">(BRASIL, 1988)</p>
                                </p>
                            </div>

                            <br><p>Observe, primeiramente, que esse dispositivo constitucional não traz propriamente uma definição do que seja o PPA. A definição do PPA, seus conceitos, sua organização, sua elaboração e sua execução deverão ser objeto da lei geral de finanças públicas prevista no <a href="http://cursos.egp.ce.gov.br/pluginfile.php/25524/mod_folder/content/0/art165.pdf?forcedownload=1" target="_blank">art. 165, §9°</a>, da Constituição Federal, ainda pendente de votação pelo Congresso Nacional. Na falta dessa lei, a regulamentação tem se dado por decretos e portarias do Poder Executivo Federal e por normas suplementares locais.</p>

                            <p class="wow fadeInRight" data-wow-delay="0.3s">A função do dispositivo constitucional é estabelecer condições que deverão ser observadas pelo PPA. Uma dessas condições é que o PPA especifique as despesas de capital a serem realizadas na vigência do plano e explicite a que objetivos e metas essas despesas estão relacionadas.</p>

                            <p>As despesas de capital são aquelas relativas, principalmente, às obras, às instalações, aos equipamentos e a material permanente, ou seja, às despesas que contribuam, diretamente, para a formação ou aquisição de um bem de capital. Temos ainda as despesas decorrentes das despesas de capital que envolvem todos os gastos relacionados à manutenção das obras, às instalações, aos equipamentos e ao custeio de sua utilização e funcionamento.</p>

                            <div class="ExemploCaixa">
                                <p class="ExemploTitulo"> Exemplo</p>
                                <div class="ExemploTexto">
                                    <p>A construção do edifício-sede de um hospital é despesa de capital da qual decorrem importantes despesas correntes, relacionadas não somente com a manutenção do prédio, mas também com o funcionamento dos serviços de saúde ao qual o prédio se destina (contratação e remuneração dos médicos, aquisição de medicamentos, pagamento de energia, água, telefone etc.).</p>
                                </div>
                            </div>

                            <br><p>Outra despesa que deve ainda estar presente no PPA é a despesa relativa ao programa de duração continuada, que inclui despesas com a manutenção de bens e serviços já existentes.</p>

                            <p>Note que a ênfase do dispositivo constitucional volta-se, claramente, para a definição de obras e equipamentos de caráter <strong>estruturante</strong> ao desenvolvimento local, estadual ou nacional, bem como ao planejamento do conjunto de serviços públicos e ações de caráter <strong>permanente</strong>. Na consideração desses dois aspectos, residem a natureza estratégica e a razão de ser do PPA, qual seja, a realização de ações estruturantes e permanentes para alavancar o desenvolvimento humano, social e econômico.</p>

                            <div class="bd-callout bd-callout-warning">
                                <p>A Constituição Federal define, ainda, que todas as despesas do PPA devem ser regionalizadas. No caso da União, a regionalização se dá pelas cinco macrorregiões definidas pelo IBGE – Norte, Nordeste, Sul, Sudeste e Centro-Oeste. Além dessas, é utilizada também a categoria <strong>“Nacional”</strong> para as despesas que não são regionalizadas ou que, por sua natureza, não se prestam à regionalização. No caso dos Estados e municípios, a regionalização se reporta a distritos definidos segundo critérios do governo local (regiões administrativas, distritos sanitários etc.).</p>
                            </div>

                            <p>O PPA tem, na concepção do sistema orçamentário brasileiro, a função primordial de orientar a programação de ações da Administração Pública, as quais deverão estar sempre em consonância com aquele. É o que se depreende de vários dispositivos constitucionais.</p>


                                                      



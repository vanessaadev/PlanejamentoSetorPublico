                            <p><strong>Objetivo:</strong> Apresentar os elementos do Plano Plurianual e a integração entre o planejamento e o orçamento.</p>

                            <p class="SubTitulo"> Integração entre Planejamento e Orçamento</p>
                            <hr>

                            <p>É uma lei orçamentária que define as ações de governo para um período igual ao do mandato presidencial (atualmente de quatro anos) e evidencia, em quadros demonstrativos, quais serão os programas de trabalho a serem implementados pelos gestores públicos durante esse período.</p>

                            <p>O PPA define as estratégias, diretrizes e metas do governo para o médio prazo. Não se trata apenas de propor ações a serem implementadas em dado período, mas sim de instituir um plano de ação que, partindo de um <strong>planejamento estratégico:</strong></p>

                            <ul class="none">
                                <li class="wow fadeInLeft" data-wow-delay="0.3s"><i class="fa fa-arrow-right CorVerdeClaroEstado"></i> discrimine os objetivos de governo a serem perseguidos durante o mandato do chefe do poder executivo;</li>
                                <li class="wow fadeInLeft" data-wow-delay="0.6s"><i class="fa fa-arrow-right CorVerdeClaroEstado"></i> estabeleça os programas setoriais a serem implementados;</li>
                                <li class="wow fadeInLeft" data-wow-delay="0.9s"><i class="fa fa-arrow-right CorVerdeClaroEstado"></i> e defina as fontes de financiamento e as metodologias de elaboração, gestão, avaliação e revisão dos programas.</li>
                            </ul>

                            <p>O PPA abrange, ainda, as ações não orçamentárias que contribuam para os objetivos dos programas.</p>

